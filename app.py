import pandas as pd
import numpy as np
import streamlit as st


import pickle

svm_classifier_loaded = pickle.load(open('forestgoblin/fake-product-review/main/svm_classifier_saved', 'rb'))

naive_bayes_loaded = pickle.load(open('forestgoblin/fake-product-review/main/naive_bayes_saved', 'rb'))


def main():
    st.title("Simple Streamlit Application")
    user_input = st.text_area("Enter your text here:", "")


    if st.button("Submit"):
      
        result1 = svm_classifier_loaded(user_input)
        result2 = naive_bayes_loaded(user_input)
       
        st.text("Output svm classifier:")
        st.write(result1)
        st.text("Output naive bayes:")
        st.write(result2)

if __name__ == "__main__":
    main()
